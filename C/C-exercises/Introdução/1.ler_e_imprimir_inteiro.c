/* Faça um programa que leia um número inteiro e o imprima */

#include <stdio.h>

int main(){
    // Declaração de variáveis
    int numero;

    // Entrada de dados
    printf("Digite um numero inteiro: ");
    scanf("%d", &numero);

    // Saída de dados
    printf("Voce digitou o numero %d", numero);

    return 0;
}
